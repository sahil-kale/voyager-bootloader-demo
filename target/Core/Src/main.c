/* USER CODE BEGIN 0 */
void udpServerInit(void)
{
    upcb_udp_server = udp_new();
    err_t err;
    ip_addr_t ip_addr = gnetif.ip_addr;
    err = udp_bind(upcb_udp_server, &ip_addr, UDP_SERVER_PORT);

    if (err == ERR_OK) {
        udp_recv(upcb_udp_server, udp_receive_callback, NULL);
    }
    else {
        udp_remove(upcb_udp_server);
    }
}

void udp_receive_callback(void *arg, struct udp_pcb *upcb, struct pbuf *p, const ip_addr_t *addr, u16_t port)
{
  server_connected = true;
  server_ip_addr = *addr;
  server_port = port;
  // Hook into process received data
  voyager_bootloader_process_receieved_packet(p->payload, p->len);
  pbuf_free(p);
}
